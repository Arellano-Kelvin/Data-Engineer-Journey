def greet(name):
    return f'Hello {name}'
def if_even(number):
    if number % 2 == 0:
        return True
    else:
        return False
def calculate_area(length, width):
    return length * width
def find_max(list_of_numbers):
    return max(list_of_numbers)
def validate_age(age):
    return age<=120
